# Integer
roll_number = 17
print(type(roll_number))

# Floating number
percentage = 79.8
print(type(percentage))

# Boolean
is_student = True
print(type(is_student))

print(name, roll_number, percentage, is_student)

percentage = 93
print(name, roll_number, percentage, is_student)

