# Slicing a string in negative indexing

text = "Hello, World!"
 
# Access the last character using -1
last_char = text[-1]
print(last_char)  # Output: !
 
# Access the second-to-last character using -2
second_last_char = text[-2]
print(second_last_char)  # Output: d
 
# Slicing using negative indexing
slice1 = text[-6:-1]
print(slice1)  # Output: World
 
