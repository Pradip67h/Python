# Slice to all..

text = "Hello, World!"
 
# Slice from index 0 to index 5 (exclusive)
slice1 = text[0:5]
print(slice1)  # Output: Hello
 
# Slice from index 7 to the end of the string
slice2 = text[7:]
print(slice2)  # Output: World!
 
# Slice from the beginning to index 5 (exclusive)
slice3 = text[:5]
print(slice3)  # Output: Hello
 
# Slice the last 6 characters of the string
slice4 = text[-6:]
print(slice4)  # Output: World!
 
# Slice from index 0 to index 11 (exclusive) with a step of 2
slice5 = text[0:11:2]
print(slice5)  # Output: text = "Hello, World!"
 
